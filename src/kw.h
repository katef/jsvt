#ifndef JVST_KW_H
#define JVST_KW_H

struct json_string;

enum kw {
	KW_SCHEMA = 1,
	KW_REF,
	KW_ID,

	KW_MULTIPLE_OF,
	KW_MAXIMUM,
	KW_EXCLUSIVE_MAXIMUM,
	KW_MINIMUM,
	KW_EXCLUSIVE_MINIMUM,
	KW_MAX_LENGTH,
	KW_MIN_LENGTH,
	KW_PATTERN,
	KW_ITEMS,
	KW_ADDITIONAL_ITEMS,
	KW_MAX_ITEMS,
	KW_MIN_ITEMS,
	KW_UNIQUE_ITEMS,
	KW_CONTAINS,
	KW_MAX_PROPERTIES,
	KW_MIN_PROPERTIES,
	KW_REQUIRED,
	KW_PROPERTIES,
	KW_PATTERN_PROPERTIES,
	KW_ADDITIONAL_PROPERTIES,
	KW_DEPENDENCIES,
	KW_PROPERTY_NAMES,
	KW_ENUM,
	KW_CONST,
	KW_TYPE,
	KW_ALL_OF,
	KW_ANY_OF,
	KW_ONE_OF,
	KW_NOT,

	/* metadata */
	KW_DEFINITIONS,
	KW_DESCRIPTION,
	KW_DEFAULT,
	KW_EXAMPLES
};

enum kw
kw_lookup(const struct json_string *str);

#endif

